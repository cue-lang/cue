// This tool generates the cueDebugHelp string used by `cue help environment`
// based on the CUE_DEBUG flags defined in internal/cuedebug/cuedebug.go.

package main

import (
	"bytes"
	"fmt"
	"go/ast"
	"go/format"
	"go/parser"
	"go/token"
	"log"
	"os"
	"strings"

	"cuelang.org/go/internal/copyright"
)

const srcPath = "../../../internal/cuedebug/cuedebug.go"

func main() {
	flags, err := extractFlags()
	if err != nil {
		log.Fatalf("failed to extract CUE_DEBUG flags: %v", err)
	}

	output := generate(flags)

	formatted, err := format.Source(output)
	if err != nil {
		fmt.Printf("****\n%s\n****\n", output)
		log.Fatalf("invalid Go source in output (see above): %v", err)
	}

	if err := os.WriteFile("environment_help_gen.go", formatted, 0o644); err != nil {
		log.Fatalf("failed to write generated file: %v", err)
	}
}

type debugFlag struct {
	// FieldName is the Go field name, such as "ToolsFlow".
	FieldName string
	// Name is the CUE_DEBUG flag name, such as "toolsflow".
	Name string
	// Comment is the field's doc comment.
	Comment string
}

// extractFlags reads the fields of the cuedebug.Config struct in declaration
// order, returning their flag names and doc comments.
func extractFlags() ([]debugFlag, error) {
	fset := token.NewFileSet()
	f, err := parser.ParseFile(fset, srcPath, nil, parser.ParseComments)
	if err != nil {
		return nil, err
	}

	var flags []debugFlag
	for _, decl := range f.Decls {
		genDecl, ok := decl.(*ast.GenDecl)
		if !ok || genDecl.Tok != token.TYPE {
			continue
		}
		for _, spec := range genDecl.Specs {
			typeSpec, ok := spec.(*ast.TypeSpec)
			if !ok || typeSpec.Name.Name != "Config" {
				continue
			}
			structType, ok := typeSpec.Type.(*ast.StructType)
			if !ok {
				continue
			}
			for _, field := range structType.Fields.List {
				if len(field.Names) == 0 {
					continue
				}
				name := field.Names[0].Name
				flags = append(flags, debugFlag{
					FieldName: name,
					Name:      strings.ToLower(name),
					Comment:   extractComment(field.Doc),
				})
			}
		}
	}
	return flags, nil
}

func generate(flags []debugFlag) []byte {
	var sb bytes.Buffer

	fmt.Fprintf(&sb, `%s

// Code generated by gen_environment_help.go; DO NOT EDIT.

package cmd

// cueDebugHelp lists the CUE_DEBUG flags for "cue help environment".
var cueDebugHelp = `, copyright.String("2026"))
	// Start the content on its own line, stripped with [1:], so that the
	// generated raw string literal stays aligned with the source.
	sb.WriteString("`\n")

	for _, flag := range flags {
		fmt.Fprintf(&sb, "\t\t%s\n", flag.Name)
		for line := range strings.SplitSeq(flag.Comment, "\n") {
			line = strings.TrimSpace(line)
			if line == "" {
				continue
			}
			// Use the flag name rather than the Go field name.
			line = strings.Replace(line, flag.FieldName, flag.Name, 1)
			// Escape backticks to keep the raw string literal valid.
			line = strings.ReplaceAll(line, "`", "`+\"`\"+`")
			fmt.Fprintf(&sb, "\t\t\t%s\n", line)
		}
	}

	sb.WriteString("`[1:]\n")
	return sb.Bytes()
}

func extractComment(group *ast.CommentGroup) string {
	if group == nil {
		return ""
	}
	var lines []string
	for _, comment := range group.List {
		text := strings.TrimSpace(strings.TrimPrefix(comment.Text, "//"))
		if text != "" {
			lines = append(lines, text)
		}
	}
	return strings.Join(lines, "\n")
}
