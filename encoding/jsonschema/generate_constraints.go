//go:build ignore

// This tool generates constraints_gen.go from constraints_graph.cue.

package main

import (
	"bytes"
	_ "embed"
	"fmt"
	"go/format"
	"os"
	"slices"
	"strings"

	"cuelang.org/go/cue"
	"cuelang.org/go/cue/cuecontext"
)

//go:embed constraints_graph.cue
var constraintsGraphCUE []byte

func main() {
	if err := run(); err != nil {
		fmt.Fprintf(os.Stderr, "generate_constraints: %v\n", err)
		os.Exit(1)
	}
}

type graph struct {
	Constraints []constraint `json:"constraints"`
	DataNodes   []dataNode   `json:"dataNodes"`
	Edges       []edge       `json:"edges"`
	NumPhases   int          `json:"numPhases"`
}

type constraint struct {
	Key      string   `json:"key"`
	Phase    int      `json:"phase"`
	Versions string   `json:"versions"`
	Func     string   `json:"func"`
	Produces []string `json:"produces"`
	Consumes []string `json:"consumes"`
}

type dataNode struct {
	Name        string   `json:"name"`
	Description string   `json:"description"`
	StateField  string   `json:"stateField"`
	Producers   []string `json:"producers"`
	Consumers   []string `json:"consumers"`
	Notes       []string `json:"notes"`
}

type edge struct {
	From string `json:"from"`
	To   string `json:"to"`
}

type cueConstraint struct {
	Key      string `json:"key"`
	Func     string `json:"fn"`
	Versions string `json:"versions"`
}

func (c cueConstraint) resolvedFunc() string {
	if c.Func == "" {
		return "constraintTODO"
	}
	return c.Func
}

type cueGraph struct {
	InternalNodes []string            `json:"internalNodes"`
	Constraints   []cueConstraint     `json:"constraints"`
	DataNodes     map[string]dataNode `json:"dataNodes"`
}

func run() error {
	outputPath := "constraints_gen.go"

	g, err := loadGraph()
	if err != nil {
		return err
	}
	computeEdges(g)
	assignPhases(g)
	if err := writeOutput(outputPath, g); err != nil {
		return err
	}
	return nil
}

func writeOutput(path string, g *graph) error {
	slices.SortFunc(g.Constraints, func(a, b constraint) int {
		return cmpString(a.Key, b.Key)
	})

	var buf bytes.Buffer
	printf := func(f string, a ...any) {
		fmt.Fprintf(&buf, "%s\n", fmt.Sprintf(f, a...))
	}
	printf("// Code generated by go generate; DO NOT EDIT.\n")
	printf("package jsonschema\n")
	printf("import \"fmt\"")
	printf("var constraints = []*constraint {")
	for _, c := range g.Constraints {
		printf("\t%s(%q, %s, %s),", phaseFuncName(c), c.Key, c.Func, c.Versions)
	}
	printf("}")
	printf("// px represents a TODO constraint that we haven't decided on a phase for yet.")
	printf("func px(name string, f constraintFunc, versions versionSet) *constraint {")
	printf("\treturn p1(name, f, versions)")
	printf("}\n")
	printf("")
	printf("func init() {")
	// Note: initialize in init rather than as a constant so that we can
	// still compile the code even with the generated file removed.
	printf("	numPhases = %d", g.NumPhases)
	printf("	for _, c := range constraints {")
	printf("		if _, ok := constraintMap[c.key]; ok {")
	printf("			panic(fmt.Errorf(\"duplicate constraint entry for %%q\", c.key))")
	printf("		}")
	printf("		constraintMap[c.key] = c")
	printf("	}")
	printf("}")
	printf("")
	for i := 0; i < g.NumPhases; i++ {
		printf("func p%d(name string, f constraintFunc, versions versionSet) *constraint {", i)
		printf("\treturn &constraint{key: name, phase: %d, versions: versions, fn: f}", i)
		printf("}")
	}

	formatted, err := format.Source(buf.Bytes())
	if err != nil {
		return err
	}
	return os.WriteFile(path, formatted, 0o666)
}

func loadGraph() (*graph, error) {
	ctx := cuecontext.New()
	v := ctx.CompileBytes(constraintsGraphCUE, cue.Filename("constraints_graph.cue"))
	if err := v.Err(); err != nil {
		return nil, err
	}
	var cg cueGraph
	if err := v.Decode(&cg); err != nil {
		return nil, err
	}
	if len(cg.Constraints) == 0 {
		return nil, fmt.Errorf("constraintsGraph.constraints is empty")
	}
	if len(cg.DataNodes) == 0 {
		return nil, fmt.Errorf("constraintsGraph.dataNodes is empty")
	}

	internalSet := map[string]bool{}
	for _, k := range cg.InternalNodes {
		internalSet[k] = true
	}

	var nodes []dataNode
	producerKeys := map[string]bool{}
	for name, dn := range cg.DataNodes {
		dn.Name = name
		nodes = append(nodes, dn)
		for _, p := range dn.Producers {
			if p == "<all-constraints>" || internalSet[p] {
				continue
			}
			producerKeys[p] = true
		}
	}
	slices.SortFunc(nodes, func(a, b dataNode) int {
		return cmpString(a.Name, b.Name)
	})

	var constraints []constraint
	for _, c := range cg.Constraints {
		fn := c.Func
		if fn == "" {
			if producerKeys[c.Key] {
				return nil, fmt.Errorf("constraint %q omits fn but appears as a producer; specify the implementation", c.Key)
			}
			fn = "constraintTODO"
		}
		constraints = append(constraints, constraint{
			Key:      c.Key,
			Func:     fn,
			Versions: c.Versions,
		})
	}

	g := &graph{Constraints: constraints, DataNodes: nodes}
	if err := mergeConstraints(g, internalSet); err != nil {
		return nil, err
	}
	return g, nil
}

func mergeConstraints(g *graph, internalSet map[string]bool) error {
	byKey := map[string]*constraint{}
	for i := range g.Constraints {
		byKey[g.Constraints[i].Key] = &g.Constraints[i]
	}

	for _, dn := range g.DataNodes {
		for _, prod := range dn.Producers {
			if prod == "<all-constraints>" {
				return fmt.Errorf("data node %q: <all-constraints> not allowed as producer", dn.Name)
			}
			if internalSet[prod] {
				continue
			}
			c := byKey[prod]
			if c == nil {
				return fmt.Errorf("data node %q refers to unknown producer %q", dn.Name, prod)
			}
			c.Produces = append(c.Produces, dn.Name)
		}
		for _, cons := range dn.Consumers {
			if cons == "<all-constraints>" {
				for i := range g.Constraints {
					g.Constraints[i].Consumes = append(g.Constraints[i].Consumes, dn.Name)
				}
				continue
			}
			if internalSet[cons] {
				continue
			}
			c := byKey[cons]
			if c == nil {
				return fmt.Errorf("data node %q refers to unknown consumer %q", dn.Name, cons)
			}
			c.Consumes = append(c.Consumes, dn.Name)
		}
	}

	for i := range g.Constraints {
		slices.Sort(g.Constraints[i].Produces)
		slices.Sort(g.Constraints[i].Consumes)
	}
	return nil
}

func computeEdges(g *graph) {
	var edges []edge
	adj := buildAdjacency(g)
	for _, dn := range g.DataNodes {
		for _, p := range dn.Producers {
			if p == "<all-constraints>" {
				continue
			}
			if _, ok := adj[p]; !ok {
				continue
			}
			edges = append(edges, edge{From: p, To: dn.Name})
		}
		for _, c := range dn.Consumers {
			if _, ok := adj[c]; !ok {
				continue
			}
			edges = append(edges, edge{From: dn.Name, To: c})
		}
	}
	g.Edges = edges
}

func assignPhases(g *graph) {
	if err := ensureAcyclic(g); err != nil {
		panic(err)
	}

	adj := buildAdjacency(g)
	dataDepth := map[string]int{}
	byName := map[string]dataNode{}
	for _, dn := range g.DataNodes {
		byName[dn.Name] = dn
	}
	consIndex := map[string]int{}
	for i, c := range g.Constraints {
		consIndex[c.Key] = i
	}

	var visitData func(name string) int
	var constraintDepth func(c constraint) int

	visitData = func(name string) int {
		if d, ok := dataDepth[name]; ok {
			return d
		}
		dn, ok := byName[name]
		if !ok {
			return 0
		}
		maxProd := 0
		for _, p := range dn.Producers {
			if p == "<all-constraints>" {
				continue
			}
			if _, ok := adj[p]; !ok {
				continue
			}
			idx, ok := consIndex[p]
			if !ok {
				continue
			}
			pd := constraintDepth(g.Constraints[idx])
			if pd > maxProd {
				maxProd = pd
			}
		}
		dataDepth[name] = maxProd + 1
		return dataDepth[name]
	}

	constraintDepth = func(c constraint) int {
		maxCons := 0
		for _, dn := range c.Consumes {
			d := visitData(dn)
			if d > maxCons {
				maxCons = d
			}
		}
		return maxCons
	}

	maxPhase := 0
	for i := range g.Constraints {
		phase := constraintDepth(g.Constraints[i])
		g.Constraints[i].Phase = phase
		if phase > maxPhase {
			maxPhase = phase
		}
	}
	g.NumPhases = maxPhase + 1
}

func ensureAcyclic(g *graph) error {
	adj := buildAdjacency(g)
	visited := map[string]bool{}
	stack := map[string]bool{}
	var walk func(n string) error
	walk = func(n string) error {
		if stack[n] {
			return fmt.Errorf("cycle detected at %s", n)
		}
		if visited[n] {
			return nil
		}
		visited[n] = true
		stack[n] = true
		for _, m := range adj[n] {
			if err := walk(m); err != nil {
				return err
			}
		}
		stack[n] = false
		return nil
	}
	for n := range adj {
		if err := walk(n); err != nil {
			return err
		}
	}
	return nil
}

func buildAdjacency(g *graph) map[string][]string {
	adj := map[string][]string{}
	for _, c := range g.Constraints {
		adj[c.Key] = nil
	}
	for _, dn := range g.DataNodes {
		adj[dn.Name] = nil
	}
	for _, dn := range g.DataNodes {
		for _, p := range dn.Producers {
			if p == "<all-constraints>" {
				continue
			}
			if _, ok := adj[p]; !ok {
				continue
			}
			adj[p] = append(adj[p], dn.Name)
		}
		for _, c := range dn.Consumers {
			if _, ok := adj[dn.Name]; !ok {
				continue
			}
			if _, ok := adj[c]; !ok {
				continue
			}
			adj[dn.Name] = append(adj[dn.Name], c)
		}
	}
	for k := range adj {
		slices.Sort(adj[k])
	}
	return adj
}

func phaseFuncName(c constraint) string {
	if c.Func == "constraintTODO" || c.Func == "constraintIgnore" {
		return "px"
	}
	return fmt.Sprintf("p%d", c.Phase)
}

func cmpString(a, b string) int {
	return stringsCompare(a, b)
}

func stringsCompare(a, b string) int {
	return strings.Compare(a, b)
}
